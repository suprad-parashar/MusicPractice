#!/usr/bin/env node
/**
 * Scans data/compositions/data/*.json and writes registry.generated.ts with static imports
 * so every JSON file is registered without manual edits.
 *
 * Slug: optional top-level string "slug" in the JSON; otherwise derived from the filename:
 *   - strip .json
 *   - [tag] in the name becomes _tag (e.g. sri_gananatha[en].json → sri_gananatha_en)
 *   - dots become underscores (e.g. mere_dholna_3.0.json → mere_dholna_3_0)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const dataDir = path.join(root, 'data/compositions/data');
const outFile = path.join(root, 'data/compositions/registry.generated.ts');

function slugFromFilename(fileBase) {
  let s = fileBase.replace(/\.json$/i, '');
  s = s.replace(/\[([^\]]+)\]/g, '_$1');
  s = s.replace(/\./g, '_');
  return s;
}

function readOptionalSlug(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8');
  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (e) {
    throw new Error(`Invalid JSON ${path.relative(root, filePath)}: ${e.message}`);
  }
  if (typeof parsed.slug === 'string') {
    const t = parsed.slug.trim();
    if (t !== '') return t;
  }
  return null;
}

function main() {
  if (!fs.existsSync(dataDir)) {
    fs.mkdirSync(dataDir, { recursive: true });
  }

  const files = fs
    .readdirSync(dataDir)
    .filter((f) => f.endsWith('.json'))
    .sort((a, b) => a.localeCompare(b));

  const rows = [];
  const slugToFile = new Map();

  for (const f of files) {
    const full = path.join(dataDir, f);
    const fromJson = readOptionalSlug(full);
    const fromName = slugFromFilename(f);
    const slug = fromJson ?? fromName;

    if (slugToFile.has(slug)) {
      console.error(
        `Duplicate composition slug "${slug}"\n  ${slugToFile.get(slug)}\n  ${f}\n` +
          'Use a different filename or set a unique "slug" in one of the JSON files.',
      );
      process.exit(1);
    }
    slugToFile.set(slug, f);

    const relImport = `./data/${f.split(path.sep).join('/')}`;
    rows.push({ slug, relImport, f });
  }

  const imports = rows.map((r, i) => `import raw_${i} from '${r.relImport}';`).join('\n');

  const entries = rows
    .map((r, i) => `  { slug: '${escapeStr(r.slug)}', raw: raw_${i} as unknown },`)
    .join('\n');

  const header = `/* eslint-disable */
/**
 * Auto-generated by scripts/generate-composition-registry.mjs — do not edit.
 * Add or change JSON in data/compositions/data/ and run \`npm run dev\` or \`npm run build\`.
 */

`;

  const body = `${imports}

export interface CompositionRegistryEntry {
  slug: string;
  raw: unknown;
}

export const COMPOSITION_REGISTRY: CompositionRegistryEntry[] = [
${entries}
];
`;

  fs.writeFileSync(outFile, header + body, 'utf8');
  console.log(`Wrote ${path.relative(root, outFile)} (${rows.length} composition(s)).`);
}

function escapeStr(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

main();
